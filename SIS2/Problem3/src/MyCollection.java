
public abstract class MyCollection {
	
	abstract public void add (int element);
	abstract public void add (int index, int element);
	abstract public void clear ();
	abstract public boolean contains (int o);
	abstract public int indexOf (int o);
	abstract public void insertElementAt (int index, int x);
	abstract public boolean isEmpty();
	abstract public void removeElementAt (int index);
	abstract public void remove (int element);
	abstract public void removeAll (int element);
	abstract public void reverse ();
	abstract public int size ();
	abstract public void set (int index, int element);
	abstract public int[] toArray ();
	abstract public String toString();
	abstract public void BubbleSort(int[] a);
	
}
